    private void JBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBCadastrarActionPerformed
        try {
            String categoria = "";

            if (this.JTFCategoria.getText().length() < 2) {
                throw new Mensagem("Nome deve conter ao menos 2 caracteres.");
            } else {
                categoria = this.JTFCategoria.getText();
            }
            if (this.objetoproduto.insertCategoriaBD(categoria)) {
                JOptionPane.showMessageDialog(null, "Categoria Cadastrada com Sucesso!");
                this.JTFCategoria.setText("");
            }
 
            System.out.println(this.objetoproduto.getMinhaListac().toString());
        } catch (Mensagem erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }//GEN-LAST:event_JBCadastrarActionPerformed

